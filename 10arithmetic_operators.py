# arithmetic operators
# they are: +,-,*,/,%(modulo for finding remainder)
print(5/2) #this will give 2.5 i.e. floating number
# to get integer output we will use floor division i.e. //
print(5//2) #this will give 2

# to calculate power we use **
print(2**3) #this will give 8

